package trump;

public class Trump3 {
	
	private int num;
	private String mark;
	
	public Trump3(int num, String mark){
		this.num = num;
		this.mark = mark;
	}
	
	public void setNum(int intNum) {
		num = intNum;
	}
	
	public int getNum() {
		return num;
	}
	
	public void setMark(String strMark) {
		mark = strMark;
	}
	
	public String getMark() {
		return mark;
	}
}